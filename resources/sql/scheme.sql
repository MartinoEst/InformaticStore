CREATE DATABASE IF NOT EXISTS `informaticstore`;
USE `informaticstore`;

-- Tables's erasing
DROP TABLE IF EXISTS `CUSTOMER`;
DROP TABLE IF EXISTS `PRODUCT`;
DROP TABLE IF EXISTS `ORDER`;
DROP TABLE IF EXISTS `PROVIDER`;
DROP TABLE IF EXISTS `SHOPPINGCART`;
DROP TABLE IF EXISTS `CONTAIN`;

-- Tables's creation
CREATE TABLE CUSTOMER
(
	IDCUSTOMER		INT AUTOINCREMENT,
	NAME			VARCHAR2(25),
	SURNAME			VARCHAR2(50),
	EMAIL			VARCHAR2(45),
	ADDRESS			VARCHAR2(100),
	PHONE			VARCHAR2(16),
	TYPE			VARCHAR2(75),
	USERNAME		VARCHAR2(40),
	PASSWORD		VARCHAR2(20),
	CONSTRAINT PK_CUST PRIMARY KEY(IDCUSTOMER)
);

CREATE TABLE PRODUCT
(
	IDPRODUCT		INT AUTOINCREMENT,
	NAME			VARCHAR2(25),
	DESCRIPTION		VARCHAR2(500),
	PRICE			NUMBER(7,2),
	AMOUNT			NUMBER(4),
	URLIMAGE		VARCHAR2(250),
	CONSTRAINT PK_PROD PRIMARY KEY(IDPRODUCT)
);

CREATE TABLE ORDER
(
	IDORDER			INT AUTOINCREMENT,
	IDCUSTOMER		NUMBER(6),
	DATEORDER		DATE
	AMOUNTPRODUCTS	NUMBER(3),
	TOTALPRICE		NUMBER(7,2),
	CONSTRAINT PK_PROD PRIMARY KEY(ID_PROD),
	CONSTRAINT FK_ORDER_CUST FOREIGN KEY(IDCUSTOMER)
							 REFERENCES CUSTOMER(IDCUSTOMER)
							 ON DELETE SET NULL;
);

CREATE TABLE PROVIDER
(
	IDPROVIDER		INT AUTOINCREMENT,
	LOCATION		VARCHAR2(100),
	MATERIAL		VARCHAR2(50),
	CONSTRAINT PK_PROV PRIMARY KEY(IDPROVIDER)
);

CREATE TABLE SHOPPINGCART
(
	IDCUSTOMER		INT AUTOINCREMENT,
	IDPRODUCT		INT AUTOINCREMENT,
	CONSTRAINT FK_SHOPP_CUST FOREIGN KEY(IDCUSTOMER)
							 REFERENCES CUSTOMER(IDCUSTOMER)
							 ON DELETE SET NULL,
	CONSTRAINT FK_SHOPP_PROD FOREIGN KEY(IDPRODUCT)
							 REFERENCES PRODUCT(IDPRODUCT)
							 ON DELETE SET NULL;
);

CREATE TABLE CONTAIN
(
	IDPRODUCT		INT AUTOINCREMENT,
	IDORDER			INT AUTOINCREMENT,
	CONSTRAINT FK_CONT_PROD FOREIGN KEY(IDPRODUCT)
							REFERENCES PRODUCT(IDPRODUCT)
							ON DELETE SET NULL,
	CONSTRAINT FK_CONT_ORDER FOREIGN KEY(IDORDER)
							 REFERENCES ORDER(IDORDER)
							 ON DELETE SET NULL;
);

